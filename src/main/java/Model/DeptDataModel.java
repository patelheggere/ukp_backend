package Model;

public class DeptDataModel {
	public String DeptID, DeptName,	DeptLat,	DeptLong,	Address;
	
	public String getDeptID() {
		return DeptID;
	}

	public void setDeptID(String deptID) {
		DeptID = deptID;
	}

	public String getDeptName() {
		return DeptName;
	}

	public void setDeptName(String deptName) {
		DeptName = deptName;
	}

	public String getDeptLat() {
		return DeptLat;
	}

	public void setDeptLat(String deptLat) {
		DeptLat = deptLat;
	}

	public String getDeptLong() {
		return DeptLong;
	}

	public void setDeptLong(String deptLong) {
		DeptLong = deptLong;
	}

	public String getAddress() {
		return Address;
	}

	public void setAddress(String address) {
		Address = address;
	}
	
}

package Model;

public class HobliModel {
	String HobliID, HobliCode, HobliName, TalukCode;

	public String getHobliID() {
		return HobliID;
	}

	public void setHobliID(String hobliID) {
		HobliID = hobliID;
	}

	public String getHobliCode() {
		return HobliCode;
	}

	public void setHobliCode(String hobliCode) {
		HobliCode = hobliCode;
	}

	public String getHobliName() {
		return HobliName;
	}

	public void setHobliName(String hobliName) {
		HobliName = hobliName;
	}

	public String getTalukCode() {
		return TalukCode;
	}

	public void setTalukCode(String talukCode) {
		TalukCode = talukCode;
	}
	
}
